function cambiarColor() {

    //Añade el color
let cambiarColor1 = document.getElementById("1");
cambiarColor1.setAttribute("class","cambiarColor");

    //Quitar el color 
   let quitarColor1 = document.getElementById("1");

   // el bind crea como una
   quitarColor1.addEventListener("mouseleave", quitarColor1.removeAttribute.bind(quitarColor1, "class"));


}


